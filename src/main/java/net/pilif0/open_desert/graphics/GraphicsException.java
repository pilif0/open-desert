package net.pilif0.open_desert.graphics;

/**
 * Represents an exception related to the graphics
 *
 * @author Filip Smola
 * @version 1.0
 */
public class GraphicsException extends RuntimeException {

    /**
     * Constructs the exception from its message
     *
     * @param message The exception message
     */
    public GraphicsException(String message) {
        super(message);
    }
}
